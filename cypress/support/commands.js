Cypress.Commands.add('visitPage', pathname => {
  cy.visit(pathname)
})

Cypress.Commands.add('validateUrl', (pathname, url = Cypress.config('baseUrl')) => {
  cy.url().should('eq', `${url}${pathname}`)
})

Cypress.Commands.add('clickButton', element => {
  cy.get(element).click()
})
